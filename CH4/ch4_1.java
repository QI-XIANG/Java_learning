package CH4;

public class ch4_1 {
    public static void main(String [] args){
        int x = 6 * 8 - 7 * 6 >> 2;
        System.out.printf("%d\n", x);
        x = 6 >> 5 * 8 - 10 * 4 + 1;
        System.out.printf("%d\n", x);
        x = 5 + 6 << 2;
        System.out.printf("%d\n", x);
        x = 7 * 3 + 8 >> 3;
        System.out.printf("%d\n", x);
    }    
}
